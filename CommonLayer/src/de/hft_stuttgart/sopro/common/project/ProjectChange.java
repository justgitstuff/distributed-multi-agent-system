package de.hft_stuttgart.sopro.common.project;

/**
 * @author Eduard Tudenhoefner - nastra@gmx.net
 * @author Sandro Degiorgi - sdegiorgi@gmail.com
 */
public class ProjectChange {

	/**
	 * Every project has its unique id
	 */
	private int projectId = 0;

	/**
	 * The project name is the filename of the problem data
	 */
	private String projectName = null;

	/**
	 * Number of Agents which participates the project
	 */
	private int numberOfAgentsOnProject;

	/**
	 * The number of negotiations.
	 */
	private int negotiationRounds;

	/**
	 * The number of proposals per round.
	 */
	private int proposalsPerRound;

	/**
	 * The used voting algorithm.
	 */
	private String votingAlgorithm;

	/**
	 * Default constructor taking three arguments.
	 * 
	 * @param projectId
	 *            The project id to set.
	 * @param projectName
	 *            The project name to set.
	 * @param numberOfAgentsOnProject
	 *            The number of Agents to set.
	 */
	public ProjectChange(int projectId, String projectName, int numberOfAgentsOnProject) {
		this.projectId = projectId;
		this.projectName = projectName;
		this.numberOfAgentsOnProject = numberOfAgentsOnProject;
	}

	public int getProjectId() {
		return projectId;
	}

	public void setProjectId(int projectId) {
		this.projectId = projectId;
	}

	public String getProjectName() {
		return projectName;
	}

	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}

	public int getNumberOfAgentsOnProject() {
		return numberOfAgentsOnProject;
	}

	public void setNumberOfAgentsOnProject(int numberOfAgentsOnProject) {
		this.numberOfAgentsOnProject = numberOfAgentsOnProject;
	}

	public int getNegotiationRounds() {
		return negotiationRounds;
	}

	public void setNegotiationRounds(int negotiationRounds) {
		this.negotiationRounds = negotiationRounds;
	}

	public int getProposalsPerRound() {
		return proposalsPerRound;
	}

	public void setProposalsPerRound(int proposalsPerRound) {
		this.proposalsPerRound = proposalsPerRound;
	}

	public String getVotingAlgorithm() {
		return votingAlgorithm;
	}

	public void setVotingAlgorithm(String votingAlgorithm) {
		this.votingAlgorithm = votingAlgorithm;
	}

}
