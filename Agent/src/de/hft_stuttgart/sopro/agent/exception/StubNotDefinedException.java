package de.hft_stuttgart.sopro.agent.exception;

import de.hft_stuttgart.sopro.agent.remote.MediatorAgentServiceStub;

/**
 * This Exception is thrown when the {@link MediatorAgentServiceStub} instance
 * is not properly defined and therefore cannot be used.
 * 
 * @author Eduard Tudenhoefner - nastra@gmx.net
 */
public class StubNotDefinedException extends Exception {

	/**
	 * The default serial version UID.
	 */
	private static final long serialVersionUID = 8310074085951850216L;

	/**
	 * Default constructor taking no arguments.
	 */
	public StubNotDefinedException() {
		super();
	}

	/**
	 * @param message
	 *            The error message.
	 */
	public StubNotDefinedException(String message) {
		super(message);
	}
}
